        private void SetSpecies(string newSpecies)
        {
            if (IsSpeciesLockedForProfile() && Profile?.Species != newSpecies)
                return;

            if (!_prototypeManager.TryIndex<SpeciesPrototype>(newSpecies, out var speciesProto))
                return;

            if (!IsSpeciesDirectlyAvailable(speciesProto))
            {
                if (!TryGetFirstAvailableModelSpecies(newSpecies, out newSpecies))
                    return;

                speciesProto = _prototypeManager.Index<SpeciesPrototype>(newSpecies);
            }

            Profile = Profile?.WithSpecies(newSpecies);

            // #Misfits Add: if the species restricts to specific jobs, auto-set those jobs to High
            // so the character doesn't fall through to overflow spawn (Wastelander) when the server
            // strips all non-restricted job priorities via EnsureValid.
            if (speciesProto.RestrictedJobs is { Count: > 0 } && Profile != null)
            {
                foreach (var restrictedJobId in speciesProto.RestrictedJobs)
                {
                    if (Profile.JobPriorities.GetValueOrDefault(restrictedJobId.Id, JobPriority.Never) == JobPriority.Never)
                        Profile = Profile.WithJobPriority(restrictedJobId.Id, JobPriority.High);
                }

                Profile = NormalizeSingleHighPriority(Profile);
            }

            OnSkinColorOnValueChanged(); // Species may have special color prefs, make sure to update it.
            Markings.SetSpecies(newSpecies); // Repopulate the markings tab as well.
            UpdateSexControls(); // Update sex for new species
            UpdateCharacterRequired();
            // Changing species provides inaccurate sliders without these
            UpdateHeightWidthSliders();
            UpdateWeight();
            UpdateSpeciesGuidebookIcon();
            UpdateTabVisibility(newSpecies); // #Misfits Change: hide tabs for restricted species
            UpdateRobotAppearanceFieldVisibility(); // #Misfits Add: apply robot-specific appearance field locks immediately after species swap.
            UpdateRobotModelSelector(); // #Misfits Add: update model selector to match selected Protectron variant.
            IsDirty = true;
            ReloadProfilePreview();
            ReloadClothes(); // Species may have job-specific gear, reload the clothes
        }

        // #Misfits Add: show a dedicated model picker for robot families with multiple variants.
        private void UpdateRobotModelSelector()
        {
            if (Profile == null)
                return;

            var familyOptions = GetActiveModelFamily();
            if (familyOptions == null)
            {
                RobotModelContainer.Visible = false;
                return;
            }

            RobotModelContainer.Visible = true;
            RobotModelButton.Clear();

            var selectedIndex = 0;
            for (var i = 0; i < familyOptions.Length; i++)
            {
                RobotModelButton.AddItem(Loc.GetString(familyOptions[i].NameLocKey), i);

                if (familyOptions[i].SpeciesId == Profile.Species)
                    selectedIndex = i;
            }

            RobotModelButton.SelectId(selectedIndex);
        }

        // #Misfits Add: returns the model option array for the current species' family, or null if none.
        private (string SpeciesId, string NameLocKey)[]? GetActiveModelFamily()
        {
            if (Profile == null)
                return null;

            // Check if the species itself is a base key in the dictionary.
            if (RobotModelFamilies.TryGetValue(Profile.Species, out var options))
                return FilterAvailableModelOptions(options);

            // Otherwise check if it's a hidden variant in any family.
            foreach (var (_, familyOpts) in RobotModelFamilies)
            {
                foreach (var (optId, _) in familyOpts)
                {
                    if (optId == Profile.Species)
                        return FilterAvailableModelOptions(familyOpts);
                }
            }

            return null;
        }

        private (string SpeciesId, string NameLocKey)[]? FilterAvailableModelOptions((string SpeciesId, string NameLocKey)[] options)
        {
            var available = options
                .Where(option => IsSpeciesDirectlyAvailable(option.SpeciesId))
                .ToArray();

            return available.Length == 0 ? null : available;
        }

