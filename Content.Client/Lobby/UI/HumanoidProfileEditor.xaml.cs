        private static bool IsProtectronModelSpecies(string speciesId)
        {
            foreach (var (_, options) in RobotModelFamilies)
            {
                foreach (var (optId, _) in options)
                {
                    if (optId == speciesId)
                        return true;
                }
            }

            return false;
        }

