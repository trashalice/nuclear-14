    /// <summary>
    /// Switches the window header and title bar between holotape-item mode and terminal mode.
    /// Holotape items display "HOLOTAPE PLAYER"; built-in terminals display "TERMLINK PROTOCOL".
    /// </summary>
    // #Misfits Add - Dynamic header switch: HOLOTAPE item vs built-in TERMINAL
    public void SetHolotapeMode(bool isHolotapeItem)
    {
        if (isHolotapeItem)
        {
            Title = "HOLOTAPE";
            HeaderLabel.Text = "ROBCO INDUSTRIES (TM) HOLOTAPE PLAYER";
        }
        else
        {
            Title = "TERMINAL";
            HeaderLabel.Text = "ROBCO INDUSTRIES (TM) TERMLINK PROTOCOL";
        }
    }

    /// <summary>
    /// Updates both the title and body content labels with green terminal text.
    /// </summary>
    public void UpdateContent(string title, string content)
    {
        // Build title message with green color
        var titleMsg = new FormattedMessage();
        if (!string.IsNullOrEmpty(title))
        {
            titleMsg.PushColor(Color.FromHex(TermGreen));
            titleMsg.AddMarkupPermissive($"[bold]> {title}[/bold]");
            titleMsg.Pop();
        }
        EntryTitleLabel.SetMessage(titleMsg);

        // Build content message with green color
        var contentMsg = new FormattedMessage();
        if (!string.IsNullOrEmpty(content))
        {
            contentMsg.PushColor(Color.FromHex(TermGreen));
            contentMsg.AddMarkupPermissive(content);
            contentMsg.Pop();
        }
        ContentLabel.SetMessage(contentMsg);
    }

    /// <summary>
    /// Updates the notes list display and enables the tab bar.
    /// Called when state includes notes (terminal has TerminalNotebookComponent).
    /// </summary>
    public void UpdateNotes(List<TerminalNoteEntry>? notes, NetUserId? viewerUserId)
    {
        _viewerUserId = viewerUserId;
        _hasNotebook = notes != null;

        // Show/hide tab bar depending on whether this terminal has a notebook, links, or database
        TabBar.Visible = _hasNotebook || _hasLinkSource || _hasDatabase || _hasOverwatch;
        NotesTabButton.Visible = _hasNotebook;

        if (notes == null)
        {
            // No notebook — make sure we're on the data tab
            SwitchToDataTab();
            return;
        }

        // Rebuild the notes list UI
        RebuildNotesList(notes);
    }

    // #Misfits Add - Populates the LINKS tab with one button per device link source port.
    // Each button fires OnInvokeLinkPort with the port ID, which the BUI sends to the server.
    /// <summary>
    /// Updates the links list display. Called when state includes device link source data.
    /// </summary>
    public void UpdateLinks(bool hasLinkSource, List<string>? portIds)
    {
        _hasLinkSource = hasLinkSource;
        LinksTabButton.Visible = _hasLinkSource;

        // Update tab bar visibility: show if notebook OR links OR database present
        TabBar.Visible = _hasNotebook || _hasLinkSource || _hasDatabase || _hasOverwatch;

        LinksList.RemoveAllChildren();

        if (!hasLinkSource || portIds == null || portIds.Count == 0)
        {
            if (LinksPanel.Visible)
                SwitchToDataTab();
            return;
        }

        foreach (var portId in portIds)
        {
            var capturedId = portId; // capture for lambda closure

            // Row container: port label + trigger button
            var row = new BoxContainer
            {
                Orientation = BoxContainer.LayoutOrientation.Horizontal,
                SeparationOverride = 8,
                HorizontalExpand = true,
            };

            // Port name label in terminal green
            var label = new RichTextLabel { HorizontalExpand = true };
            var labelMsg = new FormattedMessage();
            labelMsg.PushColor(Color.FromHex(TermGreen));
            labelMsg.AddMarkupPermissive($"[bold]> {portId}[/bold]");
            labelMsg.Pop();
            label.SetMessage(labelMsg);
            row.AddChild(label);

            // Trigger button — fires the port signal through DeviceLinkSystem
            var btn = new Button
            {
                Text = "[ ACTIVATE ]",
                MinWidth = 100,
            };
            btn.OnPressed += _ => OnInvokeLinkPort?.Invoke(capturedId);
            row.AddChild(btn);

            LinksList.AddChild(row);

            // Separator between port rows
            var sep = new PanelContainer
            {
                MinHeight = 1,
                HorizontalExpand = true,
                PanelOverride = new StyleBoxFlat(Color.FromHex("#0f3d0f")),
            };
            LinksList.AddChild(sep);
        }
    }

    // ── Tab Switching ────────────────────────────────────────────────────────

    private void SwitchToDataTab()
    {
        _showingNotes = false;
        DataPanel.Visible = true;
        NotesPanel.Visible = false;
        LinksPanel.Visible = false;
        DatabasePanel.Visible = false;
        OverwatchPanel.Visible = false;
    }

    private void SwitchToNotesTab()
    {
        _showingNotes = true;
        DataPanel.Visible = false;
        NotesPanel.Visible = true;
        LinksPanel.Visible = false;
        DatabasePanel.Visible = false;
        OverwatchPanel.Visible = false;

        // Request fresh notes from server
        OnRequestNotes?.Invoke();
    }

    // #Misfits Add - Switch to the LINKS tab showing device link port controls
    private void SwitchToLinksTab()
    {
        _showingNotes = false;
        DataPanel.Visible = false;
        NotesPanel.Visible = false;
        LinksPanel.Visible = true;
        DatabasePanel.Visible = false;
        OverwatchPanel.Visible = false;
    }

    // #Misfits Add - Switch to the DATABASE tab showing the faction-shared knowledge base
    private void SwitchToDatabaseTab()
    {
        _showingNotes = false;
        DataPanel.Visible = false;
        NotesPanel.Visible = false;
        LinksPanel.Visible = false;
        DatabasePanel.Visible = true;
        OverwatchPanel.Visible = false;

        // Reset to folder list root and request a fresh state from the server
        _dbView = DbView.FolderList;
        _dbCurrentFolder = null;
        _dbCurrentSubfolder = null;
        _dbEditingDocumentId = null;
        OnRequestDatabase?.Invoke();
        RefreshDatabaseView();
    }

    private void SwitchToOverwatchTab()
    {
        _showingNotes = false;
        DataPanel.Visible = false;
        NotesPanel.Visible = false;
        LinksPanel.Visible = false;
        DatabasePanel.Visible = false;
        OverwatchPanel.Visible = true;
        RefreshOverwatchView();
    }

    public void UpdateOverwatch(OverwatchConsoleState? state, IEye? eye)
    {
        _overwatchState = state;
        _overwatchSourceEye = eye;
        _hasOverwatch = state != null;

        OverwatchTabButton.Visible = _hasOverwatch;
        TabBar.Visible = _hasNotebook || _hasLinkSource || _hasDatabase || _hasOverwatch;

        if (state == null)
        {
            _overwatchSourceEye = null;
            UpdateOverwatchCamera(null);
            if (OverwatchPanel.Visible)
                SwitchToDataTab();

            return;
        }

        OverwatchHeaderLabel.Text = state.MonitorTitle;
        UpdateOverwatchCamera(eye);
        RefreshOverwatchView();
    }

    // ── Notes Rendering ──────────────────────────────────────────────────────

    /// <summary>
    /// Rebuilds the notes list container with all note entries.
    /// </summary>
    private void RebuildNotesList(List<TerminalNoteEntry> notes)
    {
        NotesList.RemoveAllChildren();

        if (notes.Count == 0)
        {
            // Empty state message
            var emptyLabel = new RichTextLabel();
            var emptyMsg = new FormattedMessage();
            emptyMsg.PushColor(Color.FromHex(TermDimGreen));
            emptyMsg.AddText("No notes have been written on this terminal.");
            emptyMsg.Pop();
            emptyLabel.SetMessage(emptyMsg);
            NotesList.AddChild(emptyLabel);
            return;
        }

        foreach (var note in notes)
        {
            // Container for each note
            var noteBox = new BoxContainer
            {
                Orientation = BoxContainer.LayoutOrientation.Vertical,
                SeparationOverride = 2,
            };

            // Header: author + timestamp
            var headerLabel = new RichTextLabel();
            var headerMsg = new FormattedMessage();
            headerMsg.PushColor(Color.FromHex(TermGreen));
            headerMsg.AddMarkupPermissive($"[bold]{note.AuthorName}[/bold]  —  {note.Timestamp:yyyy-MM-dd HH:mm}");
            headerMsg.Pop();
            headerLabel.SetMessage(headerMsg);
            noteBox.AddChild(headerLabel);

            // Body text
            var bodyLabel = new RichTextLabel();
            var bodyMsg = new FormattedMessage();
            bodyMsg.PushColor(Color.FromHex(TermGreen));
            bodyMsg.AddText(note.Text);
            bodyMsg.Pop();
            bodyLabel.SetMessage(bodyMsg);
            noteBox.AddChild(bodyLabel);

            // Delete button (only for the note's author)
            if (_viewerUserId != null && note.AuthorUserId == _viewerUserId)
            {
                var deleteBtn = new Button
                {
                    Text = "[ DELETE ]",
                    HorizontalAlignment = HAlignment.Left,
                };
                var noteId = note.Id; // capture for closure
                deleteBtn.OnPressed += _ => OnDeleteNote?.Invoke(noteId);
                noteBox.AddChild(deleteBtn);
            }

            // Dim separator between notes
            var sep = new PanelContainer
            {
                MinHeight = 1,
                HorizontalExpand = true,
                PanelOverride = new StyleBoxFlat(Color.FromHex("#0f3d0f")),
            };
            noteBox.AddChild(sep);

            NotesList.AddChild(noteBox);
        }
    }

    // ── Submit Handler ───────────────────────────────────────────────────────

    private void HandleSubmit()
    {
        var text = NoteInput.Text?.Trim() ?? string.Empty;
        if (string.IsNullOrEmpty(text))
            return;

        // #Misfits Tweak - Author name is now provided by the server (character entity name)
        OnSubmitNote?.Invoke(string.Empty, text);

        // Clear the input after submitting
        NoteInput.Text = string.Empty;
    }

    // ── Database Rendering ───────────────────────────────────────────────────
    // #Misfits Add - Faction-shared knowledge database tab. Stage 1 MVP:
    // folders → subfolders (1 level) → documents → revisions. Read/Write/Approve
    // gating is enforced server-side; client only hides controls based on flags.

    /// <summary>
    /// Receives a fresh database state from the server and re-renders the active view.
    /// </summary>
    private void RefreshOverwatchView()
    {
        if (_overwatchState == null)
            return;

        var search = OverwatchSearchBar.Text.Trim();
        var visibleEntries = string.IsNullOrWhiteSpace(search)
            ? _overwatchState.Personnel
            : _overwatchState.Personnel
                .Where(entry =>
                    entry.Name.Contains(search, StringComparison.OrdinalIgnoreCase) ||
                    (!string.IsNullOrWhiteSpace(entry.JobTitle) &&
                     entry.JobTitle.Contains(search, StringComparison.OrdinalIgnoreCase)) ||
                    entry.Category.Contains(search, StringComparison.OrdinalIgnoreCase) ||
                    entry.Number.ToString("D4").Contains(search, StringComparison.OrdinalIgnoreCase))
                .ToList();

        OverwatchNoPersonnelLabel.Visible = visibleEntries.Count == 0;
        OverwatchStopWatchingButton.Disabled = _overwatchState.WatchedNumber == null;
        SetTerminalMarkup(OverwatchRosterSummaryLabel,
            _overwatchState.Personnel.Count == 0
                ? $"[color={TermDimGreen}]No linked personnel detected.[/color]"
                : $"[color={TermGreen}]{visibleEntries.Count} of {_overwatchState.Personnel.Count} linked personnel visible.[/color]");

        var hasCurrent = false;
        if (_overwatchState.WatchedNumber != null)
        {
            foreach (var entry in _overwatchState.Personnel)
            {
                if (entry.Number != _overwatchState.WatchedNumber.Value)
                    continue;

                hasCurrent = true;
                break;
            }
        }

        if (hasCurrent)
        {
            SetTerminalMarkup(OverwatchStatusLabel,
                $"[color={TermGreen}]SOUND[/color] live   [color={TermGreen}]CAMERA[/color] live");
        }
        else if (_overwatchState.WatchedNumber != null && HasLastKnownOverwatchPosition())
        {
            SetTerminalMarkup(OverwatchStatusLabel,
                $"[color={TermGreen}]SOUND[/color] lost   [color={TermGreen}]CAMERA[/color] lost   [color={TermDimGreen}]Last known:[/color] {FormattedMessage.EscapeText(GetLastKnownOverwatchText())}");
        }
        else
        {
            SetTerminalMarkup(OverwatchStatusLabel,
                $"[color={TermGreen}]SOUND[/color] idle   [color={TermGreen}]CAMERA[/color] idle");
        }

        OverwatchPersonnelList.RemoveAllChildren();
        foreach (var group in visibleEntries
                     .OrderBy(entry => entry.CategorySortOrder)
                     .ThenBy(entry => entry.Name, StringComparer.OrdinalIgnoreCase)
                     .GroupBy(entry => entry.Category))
        {
            var groupName = group.Key;
            var bodyContents = new BoxContainer
            {
                Orientation = BoxContainer.LayoutOrientation.Vertical,
                SeparationOverride = 6,
                Margin = new Thickness(8, 4, 8, 8),
                HorizontalExpand = true,
            };

            foreach (var entry in group)
            {
                bodyContents.AddChild(MakeOverwatchRow(entry));
            }

            var heading = new CollapsibleHeading($"{groupName} [{group.Count()}]")
            {
                MinHeight = 28,
                HorizontalExpand = true,
            };
            heading.AddStyleClass(ContainerButton.StyleClassButton);
            heading.Label.HorizontalExpand = true;
            heading.Label.HorizontalAlignment = HAlignment.Left;

            var body = new CollapsibleBody
            {
                HorizontalExpand = true,
                Children =
                {
                    bodyContents
                }
            };

            var collapsible = new Collapsible(heading, body)
            {
                HorizontalExpand = true,
                BodyVisible = !_overwatchGroupExpanded.TryGetValue(groupName, out var expanded)
                    ? string.IsNullOrWhiteSpace(search) || group.Any(entry => entry.IsCurrentTarget)
                    : expanded,
            };

            heading.OnToggled += args => _overwatchGroupExpanded[groupName] = args.Pressed;
            OverwatchPersonnelList.AddChild(collapsible);
        }
    }

    private Control MakeOverwatchRow(OverwatchConsoleEntry entry)
    {
        var active = entry.IsCurrentTarget;
        var rowStyle = new StyleBoxFlat
        {
            BackgroundColor = Color.FromHex(active ? "#0a130a" : "#050805"),
            BorderColor = Color.FromHex(active ? TermGreen : "#0f3d0f"),
            BorderThickness = active ? new Thickness(4, 1, 1, 1) : new Thickness(1),
        };

        var row = new ContainerButton
        {
            HorizontalExpand = true,
            StyleBoxOverride = rowStyle,
        };
        row.OnPressed += _ => OnOverwatchAction?.Invoke(
            active ? OverwatchConsoleMessageType.Unwatch : OverwatchConsoleMessageType.Watch,
            entry.Number);

        var contents = new BoxContainer
        {
            Orientation = BoxContainer.LayoutOrientation.Vertical,
            SeparationOverride = 6,
            Margin = new Thickness(6, 4, 6, 4),
        };
        row.AddChild(contents);

        var entryName = entry.Name ?? "UNKNOWN";
        var job = string.IsNullOrWhiteSpace(entry.JobTitle) ? "Unknown role" : entry.JobTitle!;

        var header = new BoxContainer
        {
            Orientation = BoxContainer.LayoutOrientation.Horizontal,
            SeparationOverride = 8,
            HorizontalExpand = true,
        };
        contents.AddChild(header);

        var title = new RichTextLabel { HorizontalExpand = true };
        SetTerminalMarkup(title,
            $"[color={TermGreen}][bold]> {FormattedMessage.EscapeText(entryName)}[/bold][/color] [color={TermDimGreen}]#{entry.Number:D4}[/color]");
        header.AddChild(title);

        var detail = new RichTextLabel
        {
            HorizontalExpand = true,
        };
        SetTerminalMarkup(detail,
            $"[color={TermDimGreen}]{FormattedMessage.EscapeText(job)}[/color]");
        contents.AddChild(detail);

        var bottom = new BoxContainer
        {
            Orientation = BoxContainer.LayoutOrientation.Horizontal,
            SeparationOverride = 6,
            HorizontalExpand = true,
        };
        contents.AddChild(bottom);

        var healthBar = new ProgressBar
        {
            MinValue = 0f,
            MaxValue = 1f,
            Value = entry.Health,
            MinHeight = 12,
            HorizontalExpand = true,
        };
        UpdateHealthBarStyle(healthBar, entry.Health, entry.State);
        bottom.AddChild(healthBar);

        var state = new Label
        {
            Text = GetOverwatchStateLabel(entry.State),
            StyleClasses = { "monospace" },
            FontColorOverride = GetOverwatchStateColor(entry.State),
            MinWidth = 88,
            HorizontalAlignment = HAlignment.Right,
        };
        bottom.AddChild(state);

        return row;
    }

    private void UpdateOverwatchCamera(IEye? eye)
    {
        var hasFeed = eye != null;
        OverwatchCameraView.Eye = hasFeed ? _overwatchFeedEye : _overwatchDefaultEye;
        OverwatchCameraView.Visible = hasFeed;
        OverwatchCameraFallback.Visible = !hasFeed;

        var hasCurrent = TryGetCurrentOverwatchEntry(out _);
        var hasLastKnown = _overwatchState?.WatchedNumber != null && HasLastKnownOverwatchPosition();
        OverwatchCameraFallbackLabel.Text = hasCurrent
            ? "CAMERA FEED UNAVAILABLE"
            : hasLastKnown
                ? $"FEED LOST\nLAST KNOWN {GetLastKnownOverwatchText()}"
                : "NO ACTIVE CAMERA FEED";
        OverwatchSignalLabel.Text = hasCurrent
            ? "SECURE LINK ACTIVE"
            : hasLastKnown
                ? "LAST KNOWN LOCK"
                : "SECURE LINK STANDBY";
        OverwatchSignalLabel.FontColorOverride = Color.FromHex(hasCurrent || hasLastKnown ? TermGreen : TermDimGreen);
        SyncOverwatchFeedEye();
    }

    private void SyncOverwatchFeedEye()
    {
        if (_overwatchSourceEye == null)
            return;

        _overwatchFeedEye.Position = _overwatchSourceEye.Position;
        _overwatchFeedEye.Offset = _overwatchSourceEye.Offset;
        _overwatchFeedEye.Rotation = _overwatchSourceEye.Rotation;
        _overwatchFeedEye.Zoom = _overwatchSourceEye.Zoom;
        _overwatchFeedEye.DrawFov = _overwatchSourceEye.DrawFov;
        _overwatchFeedEye.DrawLight = _overwatchSourceEye.DrawLight;
    }

    private bool TryGetCurrentOverwatchEntry(out OverwatchConsoleEntry entry)
    {
        entry = default;

        if (_overwatchState?.WatchedNumber == null)
            return false;

        foreach (var personnel in _overwatchState.Personnel)
        {
            if (personnel.Number != _overwatchState.WatchedNumber.Value)
                continue;

            entry = personnel;
            return true;
        }

        return false;
    }

    private bool HasLastKnownOverwatchPosition()
    {
        return _overwatchState?.LastKnownX != null &&
               _overwatchState.LastKnownY != null &&
               !string.IsNullOrWhiteSpace(_overwatchState.LastKnownTimestamp);
    }

    private string GetLastKnownOverwatchText()
    {
        if (_overwatchState?.LastKnownX == null ||
            _overwatchState.LastKnownY == null ||
            string.IsNullOrWhiteSpace(_overwatchState.LastKnownTimestamp))
        {
            return "UNAVAILABLE";
        }

        return $"{_overwatchState.LastKnownX.Value:0.0}, {_overwatchState.LastKnownY.Value:0.0} @ {_overwatchState.LastKnownTimestamp}";
    }

    private void UpdateHealthBarStyle(ProgressBar bar, float health, MobState state)
    {
        bar.BackgroundStyleBoxOverride ??= new StyleBoxFlat(Color.FromHex("#081108"));
        bar.ForegroundStyleBoxOverride ??= new StyleBoxFlat(Color.FromHex(TermGreen));

        var foreground = (StyleBoxFlat) bar.ForegroundStyleBoxOverride;
        foreground.BackgroundColor = state switch
        {
