    public void UpdateDatabase(TerminalDatabaseState? state)
    {
        _databaseState = state;
        // #Misfits Change - DATABASE tab is shown whenever the server sent any database
        // state (which is now always, on every terminal). When the viewer lacks read
        // access, the panel itself renders a giant NO ACCESS message instead of being hidden.
        _hasDatabase = state != null;

        DatabaseTabButton.Visible = _hasDatabase;
        TabBar.Visible = _hasNotebook || _hasLinkSource || _hasDatabase || _hasOverwatch;

        if (state == null)
        {
            // No DB component on this terminal — make sure we aren't stuck on the tab.
            if (DatabasePanel.Visible)
                SwitchToDataTab();
            return;
        }

        // Header reflects faction display name + accent color
        DatabaseHeaderLabel.Text = state.DisplayName;
        if (TryParseColor(state.AccentColor, out var accent))
            DatabaseHeaderLabel.FontColorOverride = accent;

        // If the server included an open document, jump straight to the viewer
        if (state.OpenDocument != null)
        {
            _dbView = DbView.DocumentViewer;
            _dbEditingDocumentId = null;
        }

        RefreshDatabaseView();
    }

    /// <summary>
    /// Re-renders the database panel based on current navigation state (_dbView, etc.).
    /// </summary>
    private void RefreshDatabaseView()
    {
        if (_databaseState == null)
            return;

        // Reset visibility
        DatabaseBrowserScroll.Visible = false;
        DatabaseDocScroll.Visible = false;
        DatabaseEditor.Visible = false;
        DatabaseList.RemoveAllChildren();
        DatabaseActionsBar.RemoveAllChildren();

        // #Misfits Add - NO ACCESS sentinel: if the viewer doesn't match any faction
        // database's read access tags, the server returns a state with CanRead=false.
        // Render a giant red ACCESS DENIED panel and bail out (no folders/editor).
        if (!_databaseState.CanRead)
        {
            DatabaseBrowserScroll.Visible = true;
            DatabaseBreadcrumbLabel.SetMessage(FormattedMessage.FromMarkupPermissive("[color=#FF3333][bold]> ACCESS DENIED[/bold][/color]"));

            var noAccess = new RichTextLabel
            {
                HorizontalAlignment = HAlignment.Center,
                VerticalAlignment = VAlignment.Center,
                Margin = new Thickness(0, 80, 0, 0),
            };
            noAccess.SetMessage(FormattedMessage.FromMarkupPermissive(
                "[color=#FF3333][bold][font size=32]NO ACCESS[/font][/bold][/color]\n\n" +
                "[color=#AA2222]No authorized faction database matches your ID.[/color]"));
            DatabaseList.AddChild(noAccess);
            return;
        }

        // Breadcrumb
        DatabaseBreadcrumbLabel.SetMessage(BuildBreadcrumb());

        switch (_dbView)
        {
            case DbView.FolderList:
                DatabaseBrowserScroll.Visible = true;
                RenderFolderList();
                break;
            case DbView.SubfolderList:
                DatabaseBrowserScroll.Visible = true;
                RenderSubfolderList();
                break;
            case DbView.DocumentList:
                DatabaseBrowserScroll.Visible = true;
                RenderDocumentList();
                break;
            case DbView.DocumentViewer:
                DatabaseDocScroll.Visible = true;
                RenderDocumentViewer();
                break;
            case DbView.EditorCreateFolder:
            case DbView.EditorCreateDoc:
            case DbView.EditorEditDoc:
                DatabaseEditor.Visible = true;
                RenderEditor();
                break;
        }
    }

    private FormattedMessage BuildBreadcrumb()
    {
        var msg = new FormattedMessage();
        msg.PushColor(Color.FromHex(TermDimGreen));
        msg.AddMarkupPermissive("[bold]> ROOT[/bold]");

        if (_databaseState == null)
        {
            msg.Pop();
            return msg;
        }

        if (_dbCurrentFolder != null)
        {
            var folder = FindFolder(_dbCurrentFolder.Value);
            if (folder != null)
                msg.AddMarkupPermissive($" / [bold]{FormattedMessage.EscapeText(folder.Name)}[/bold]");
        }
        if (_dbCurrentSubfolder != null)
        {
            var sub = FindSubfolder(_dbCurrentFolder, _dbCurrentSubfolder.Value);
            if (sub != null)
                msg.AddMarkupPermissive($" / [bold]{FormattedMessage.EscapeText(sub.Name)}[/bold]");
        }
        if (_dbView == DbView.DocumentViewer && _databaseState.OpenDocument != null)
            msg.AddMarkupPermissive($" / [bold]{FormattedMessage.EscapeText(_databaseState.OpenDocument.Title)}[/bold]");

        msg.Pop();
        return msg;
    }

    private DatabaseFolderSummary? FindFolder(Guid id)
    {
        if (_databaseState == null)
            return null;
        foreach (var f in _databaseState.Folders)
            if (f.FolderId == id) return f;
        return null;
    }

    private DatabaseSubfolderSummary? FindSubfolder(Guid? folderId, Guid id)
    {
        if (folderId == null) return null;
        var folder = FindFolder(folderId.Value);
        if (folder == null) return null;
        foreach (var s in folder.Subfolders)
            if (s.SubfolderId == id) return s;
        return null;
    }

    private void RenderFolderList()
    {
        if (_databaseState == null) return;

        if (_databaseState.Folders.Count == 0)
            DatabaseList.AddChild(MakeDimLabel("No folders. Create one to begin."));

        foreach (var f in _databaseState.Folders)
        {
            var capId = f.FolderId;
            var capName = f.Name;
            var deleted = f.Deleted;
            // #Misfits Add - Admin-marked folders can only be deleted/restored by Admin tier.
            var isAdmin = f.IsAdmin;
            var canModify = isAdmin ? _databaseState.CanAdmin : _databaseState.CanLeadership;

            var row = new BoxContainer { Orientation = BoxContainer.LayoutOrientation.Horizontal, SeparationOverride = 6, HorizontalExpand = true };
            // #Misfits Tweak - Prefix with faction admin label badge for marked folders.
            var adminBadge = isAdmin ? $"[{_databaseState.AdminLabel}] " : string.Empty;
            var btn = new Button
            {
                Text = deleted ? $"{adminBadge}[ {capName} ] [DELETED]" : $"{adminBadge}[ {capName} ]",
                HorizontalExpand = true,
                Disabled = deleted,
            };
            btn.OnPressed += _ =>
            {
                _dbCurrentFolder = capId;
                _dbCurrentSubfolder = null;
                _dbView = DbView.SubfolderList;
                RefreshDatabaseView();
            };
            row.AddChild(btn);

            // #Misfits Change - Delete/restore gated on Leadership (or Admin if marked).
            if (!deleted && canModify)
            {
                var del = new Button { Text = "[ DEL ]", MinWidth = 60 };
                del.OnPressed += _ => OnDeleteDatabaseFolder?.Invoke(capId, null);
                row.AddChild(del);
            }
            if (deleted && canModify)
            {
                var rest = new Button { Text = "[ RESTORE ]", MinWidth = 80 };
                rest.OnPressed += _ => OnRestoreDatabaseEntry?.Invoke(capId, null, null, null);
                row.AddChild(rest);
            }

            DatabaseList.AddChild(row);
        }

        // #Misfits Change - Root folder creation is Leadership-only (Tier 7+). Tier 6
        // (write tag) cannot create root entries; they may only add inside existing roots.
        if (_databaseState.CanLeadership)
        {
            var create = new Button { Text = "[ NEW FOLDER ]", MinWidth = 120 };
            create.OnPressed += _ =>
            {
                _dbView = DbView.EditorCreateFolder;
                _dbEditorParentFolder = null;
                RefreshDatabaseView();
            };
            DatabaseActionsBar.AddChild(create);
        }
    }

    private void RenderSubfolderList()
    {
        if (_databaseState == null || _dbCurrentFolder == null) return;
        var folder = FindFolder(_dbCurrentFolder.Value);
        if (folder == null) { _dbView = DbView.FolderList; RefreshDatabaseView(); return; }

        // "Up" button to root
        DatabaseList.AddChild(MakeNavButton("[ .. ]", () =>
        {
            _dbView = DbView.FolderList;
            _dbCurrentFolder = null;
            RefreshDatabaseView();
        }));

        // Documents at folder root
        DatabaseList.AddChild(MakeDimLabel("─ DOCUMENTS ─"));
        var rootDocs = 0;
        foreach (var d in folder.Documents)
        {
            rootDocs++;
            DatabaseList.AddChild(MakeDocRow(d, folder.FolderId, null));
        }
        if (rootDocs == 0)
            DatabaseList.AddChild(MakeDimLabel("(none)"));

        // Subfolders
        DatabaseList.AddChild(MakeDimLabel("─ SUBFOLDERS ─"));
        if (folder.Subfolders.Count == 0)
            DatabaseList.AddChild(MakeDimLabel("(none)"));
        foreach (var s in folder.Subfolders)
        {
            var capSub = s.SubfolderId;
            var deleted = s.Deleted;
            // #Misfits Add - Subfolder lives under a root folder; inherits its Admin gating.
            var canModify = folder.IsAdmin ? _databaseState.CanAdmin : _databaseState.CanLeadership;
            var row = new BoxContainer { Orientation = BoxContainer.LayoutOrientation.Horizontal, SeparationOverride = 6, HorizontalExpand = true };
            var btn = new Button
            {
                Text = deleted ? $"[ {s.Name} ] [DELETED]" : $"[ {s.Name} ]",
                HorizontalExpand = true,
                Disabled = deleted,
            };
            btn.OnPressed += _ =>
            {
                _dbCurrentSubfolder = capSub;
                _dbView = DbView.DocumentList;
                RefreshDatabaseView();
            };
            row.AddChild(btn);

            if (!deleted && canModify)
            {
                var del = new Button { Text = "[ DEL ]", MinWidth = 60 };
                del.OnPressed += _ => OnDeleteDatabaseFolder?.Invoke(folder.FolderId, capSub);
                row.AddChild(del);
            }
            if (deleted && canModify)
            {
                var rest = new Button { Text = "[ RESTORE ]", MinWidth = 80 };
                rest.OnPressed += _ => OnRestoreDatabaseEntry?.Invoke(null, folder.FolderId, capSub, null);
                row.AddChild(rest);
            }
            DatabaseList.AddChild(row);
        }

        // Action bar: new subfolder + new document
        if (_databaseState.CanWrite)
        {
            var newSub = new Button { Text = "[ NEW SUBFOLDER ]", MinWidth = 140 };
            newSub.OnPressed += _ =>
            {
                _dbView = DbView.EditorCreateFolder;
                _dbEditorParentFolder = folder.FolderId;
                RefreshDatabaseView();
            };
            DatabaseActionsBar.AddChild(newSub);

            var newDoc = new Button { Text = "[ NEW DOCUMENT ]", MinWidth = 140 };
            newDoc.OnPressed += _ =>
            {
                _dbView = DbView.EditorCreateDoc;
                _dbEditingDocumentId = null;
                RefreshDatabaseView();
            };
            DatabaseActionsBar.AddChild(newDoc);
        }
    }

    private void RenderDocumentList()
    {
        if (_databaseState == null || _dbCurrentFolder == null || _dbCurrentSubfolder == null) return;
        var folder = FindFolder(_dbCurrentFolder.Value);
        var sub = folder == null ? null : FindSubfolder(_dbCurrentFolder, _dbCurrentSubfolder.Value);
        if (folder == null || sub == null) { _dbView = DbView.SubfolderList; RefreshDatabaseView(); return; }

        DatabaseList.AddChild(MakeNavButton("[ .. ]", () =>
        {
            _dbCurrentSubfolder = null;
            _dbView = DbView.SubfolderList;
            RefreshDatabaseView();
        }));

        if (sub.Documents.Count == 0)
            DatabaseList.AddChild(MakeDimLabel("No documents in this subfolder."));

        foreach (var d in sub.Documents)
            DatabaseList.AddChild(MakeDocRow(d, folder.FolderId, sub.SubfolderId));

        if (_databaseState.CanWrite)
        {
            var newDoc = new Button { Text = "[ NEW DOCUMENT ]", MinWidth = 140 };
            newDoc.OnPressed += _ =>
            {
                _dbView = DbView.EditorCreateDoc;
                _dbEditingDocumentId = null;
                RefreshDatabaseView();
            };
            DatabaseActionsBar.AddChild(newDoc);
        }
    }

    private Control MakeDocRow(DatabaseDocumentSummary d, Guid folderId, Guid? subfolderId)
    {
        var capId = d.DocumentId;
        var deleted = d.Deleted;
        // #Misfits Add - Doc inherits Admin gating from its parent root folder OR its own IsAdmin flag.
        var parentFolder = FindFolder(folderId);
        var parentIsAdmin = parentFolder?.IsAdmin ?? false;
        var effectiveAdmin = parentIsAdmin || d.IsAdmin;
        var canModify = effectiveAdmin ? _databaseState!.CanAdmin : _databaseState!.CanLeadership;
        var row = new BoxContainer { Orientation = BoxContainer.LayoutOrientation.Horizontal, SeparationOverride = 6, HorizontalExpand = true };

        // #Misfits Add - Admin badge prefix on the doc row when this doc is independently Admin-protected
        // (parent's badge already shown on the folder row, so only flag standalone doc-admin here).
        var docAdminBadge = (d.IsAdmin && !parentIsAdmin) ? $"[{_databaseState!.AdminLabel}] " : string.Empty;
        var btn = new Button
        {
            Text = deleted
                ? $"{docAdminBadge}> {d.Title} [DELETED]"
                : $"{docAdminBadge}> {d.Title}  (rev {d.RevisionCount})",
            HorizontalExpand = true,
            Disabled = deleted,
        };
        btn.OnPressed += _ => OnOpenDatabaseDocument?.Invoke(capId);
        row.AddChild(btn);

        if (!deleted && canModify)
        {
            var del = new Button { Text = "[ DEL ]", MinWidth = 60 };
            del.OnPressed += _ => OnDeleteDatabaseDocument?.Invoke(capId);
            row.AddChild(del);
        }
        if (deleted && canModify)
        {
            var rest = new Button { Text = "[ RESTORE ]", MinWidth = 80 };
            rest.OnPressed += _ => OnRestoreDatabaseEntry?.Invoke(null, null, null, capId);
            row.AddChild(rest);
        }

        return row;
    }

    private void RenderDocumentViewer()
    {
        if (_databaseState?.OpenDocument == null)
        {
            // No doc loaded; bail to last known view.
            _dbView = _dbCurrentSubfolder != null ? DbView.DocumentList
                    : _dbCurrentFolder    != null ? DbView.SubfolderList
                    : DbView.FolderList;
            RefreshDatabaseView();
            return;
        }

        var doc = _databaseState.OpenDocument;

        var title = new FormattedMessage();
        title.PushColor(Color.FromHex(TermGreen));
        title.AddMarkupPermissive($"[bold]{FormattedMessage.EscapeText(doc.Title)}[/bold]");
        title.Pop();
        DatabaseDocTitle.SetMessage(title);

        var meta = new FormattedMessage();
        meta.PushColor(Color.FromHex(TermDimGreen));
        var lastRev = doc.Revisions.Count > 0 ? doc.Revisions[doc.Revisions.Count - 1] : null;
        var lastEditor = lastRev?.AuthorCharName ?? doc.CreatedByCharName;
        var lastTime = lastRev?.Timestamp ?? doc.CreatedAt;
        meta.AddText($"Author: {doc.CreatedByCharName}   |   Last edit: {lastEditor} @ {lastTime:yyyy-MM-dd HH:mm} UTC   |   Revisions: {doc.Revisions.Count}");
        meta.Pop();
        DatabaseDocMeta.SetMessage(meta);

        var body = new FormattedMessage();
        body.PushColor(Color.FromHex(TermGreen));
        body.AddMarkupPermissive(doc.Body);
        body.Pop();
        DatabaseDocBody.SetMessage(body);

        // Action bar: back, edit, rollback list
        var back = new Button { Text = "[ BACK ]", MinWidth = 80 };
        back.OnPressed += _ =>
        {
            _dbView = _dbCurrentSubfolder != null ? DbView.DocumentList
                    : _dbCurrentFolder    != null ? DbView.SubfolderList
                    : DbView.FolderList;
            RefreshDatabaseView();
        };
        DatabaseActionsBar.AddChild(back);

        if (_databaseState.CanWrite && !doc.Deleted)
        {
            // #Misfits Tweak - Edit on Admin-protected docs requires Admin tier (server enforces same).
            var docCanEdit = doc.IsAdmin ? _databaseState.CanAdmin : _databaseState.CanWrite;
            if (docCanEdit)
            {
                var edit = new Button { Text = "[ EDIT ]", MinWidth = 80 };
                edit.OnPressed += _ =>
                {
                    _dbView = DbView.EditorEditDoc;
                    _dbEditingDocumentId = doc.DocumentId;
                    RefreshDatabaseView();
                };
                DatabaseActionsBar.AddChild(edit);
            }
        }

        // #Misfits Add - [ EXPORT ] turns the current revision into a physical holotape
        // (placed into the actor's hand). Anyone with read access can export; deleted docs
        // are export-blocked client-side (server enforces leadership-only on deleted).
        if (!doc.Deleted)
        {
            var capExportDocId = doc.DocumentId;
            var export = new Button
            {
                Text = "[ EXPORT ]",
                MinWidth = 100,
                ToolTip = "Spawn a holotape carrying this document's title and body. Use it on a terminal or Pip-Boy to read on the go.",
            };
            export.OnPressed += _ => OnExportDatabaseDocument?.Invoke(capExportDocId);
            DatabaseActionsBar.AddChild(export);
        }

        if (_databaseState.CanLeadership && doc.Revisions.Count > 1)
        {
            // Show a rollback button per non-current revision
            foreach (var r in doc.Revisions)
            {
                if (r.RevisionNumber == doc.Revisions[doc.Revisions.Count - 1].RevisionNumber)
                    continue; // skip current
                var capRev = r.RevisionNumber;
                var capDocId = doc.DocumentId;
                var rb = new Button
                {
                    Text = $"[ ROLLBACK r{capRev} ]",
                    MinWidth = 120,
                };
                rb.OnPressed += _ => OnRollbackDatabaseDocument?.Invoke(capDocId, capRev);
                DatabaseActionsBar.AddChild(rb);
            }
        }
    }

    private void RenderEditor()
    {
        // Configure prompts and prefill
        DatabaseEditorTitleInput.Visible = _dbView == DbView.EditorCreateDoc || _dbView == DbView.EditorCreateFolder;
        DatabaseEditorBodyInput.Visible = _dbView != DbView.EditorCreateFolder;

        // #Misfits Add - Toolbar follows the same visibility rule as the body input
        // (hidden when creating a folder, since folders have no rich-text body).
        DatabaseEditorToolbar.Visible = _dbView != DbView.EditorCreateFolder;

        switch (_dbView)
        {
            case DbView.EditorCreateFolder:
                DatabaseEditorPrompt.Text = _dbEditorParentFolder == null
                    ? "NEW FOLDER (top-level)"
                    : "NEW SUBFOLDER";
                DatabaseEditorTitleInput.Text = string.Empty;
                DatabaseEditorTitleInput.PlaceHolder = "Folder name (max 64)";
                // #Misfits Tweak - Admin checkbox visible on ROOT folder creation for any
                // Leadership-tier (or higher) actor. Protection itself still gates editing/
                // deletion behind Admin tier — this lets heads protect their own stuff so peer
                // leadership can't trample it.
                var canShowAdminToggle = _dbEditorParentFolder == null
                                         && _databaseState != null
                                         && _databaseState.CanLeadership;
                DatabaseEditorAdminToggle.Visible = canShowAdminToggle;
                DatabaseEditorAdminToggle.Pressed = false;
                if (canShowAdminToggle)
                    DatabaseEditorAdminToggle.Text = $"[ \u2610  MARK AS {_databaseState!.AdminLabel} ]";
                break;
            case DbView.EditorCreateDoc:
                DatabaseEditorPrompt.Text = "NEW DOCUMENT";
                DatabaseEditorTitleInput.Text = string.Empty;
                DatabaseEditorTitleInput.PlaceHolder = "Title (max 64 chars)";
                DatabaseEditorBodyInput.TextRope = new Rope.Leaf(string.Empty);
                // #Misfits Tweak - Admin checkbox is also offered for documents (independent
                // per-document protection). Visible for Leadership tier or higher; the resulting
                // protection still requires Admin tier to bypass.
                var canShowDocAdminToggle = _databaseState != null && _databaseState.CanLeadership;
                DatabaseEditorAdminToggle.Visible = canShowDocAdminToggle;
                DatabaseEditorAdminToggle.Pressed = false;
                if (canShowDocAdminToggle)
                    DatabaseEditorAdminToggle.Text = $"[ \u2610  MARK AS {_databaseState!.AdminLabel} ]";
                break;
            case DbView.EditorEditDoc:
                DatabaseEditorPrompt.Text = "EDIT DOCUMENT (creates new revision)";
                var doc = _databaseState?.OpenDocument;
                DatabaseEditorBodyInput.TextRope = new Rope.Leaf(doc?.Body ?? string.Empty);
                DatabaseEditorAdminToggle.Visible = false;
                break;
        }

        var back = new Button { Text = "[ BACK ]", MinWidth = 80 };
        back.OnPressed += _ => HandleDatabaseEditorCancel();
        DatabaseActionsBar.AddChild(back);
    }

    private void HandleDatabaseEditorSubmit()
    {
        if (_databaseState == null) return;

        switch (_dbView)
        {
            case DbView.EditorCreateFolder:
            {
                var name = DatabaseEditorTitleInput.Text?.Trim() ?? string.Empty;
                if (string.IsNullOrEmpty(name)) return;
                // #Misfits Add - Admin flag only meaningful when toggle was visible (root + Admin tier).
                var markAdmin = DatabaseEditorAdminToggle.Visible && DatabaseEditorAdminToggle.Pressed;
                OnCreateDatabaseFolder?.Invoke(_dbEditorParentFolder, name, markAdmin);
                _dbView = _dbEditorParentFolder == null ? DbView.FolderList : DbView.SubfolderList;
                _dbEditorParentFolder = null;
                RefreshDatabaseView();
                break;
            }
            case DbView.EditorCreateDoc:
            {
                if (_dbCurrentFolder == null) return;
                var title = DatabaseEditorTitleInput.Text?.Trim() ?? string.Empty;
                var body = Rope.Collapse(DatabaseEditorBodyInput.TextRope);
                if (string.IsNullOrEmpty(title) || string.IsNullOrEmpty(body)) return;
                // #Misfits Add - Per-document Admin flag, only meaningful when toggle was visible.
                var docMarkAdmin = DatabaseEditorAdminToggle.Visible && DatabaseEditorAdminToggle.Pressed;
                OnCreateDatabaseDocument?.Invoke(_dbCurrentFolder.Value, _dbCurrentSubfolder, title, body, docMarkAdmin);
                _dbView = _dbCurrentSubfolder != null ? DbView.DocumentList : DbView.SubfolderList;
                RefreshDatabaseView();
                break;
            }
            case DbView.EditorEditDoc:
            {
                if (_dbEditingDocumentId == null) return;
                var body = Rope.Collapse(DatabaseEditorBodyInput.TextRope);
                if (string.IsNullOrEmpty(body)) return;
                OnEditDatabaseDocument?.Invoke(_dbEditingDocumentId.Value, body);
                _dbView = DbView.DocumentViewer;
                RefreshDatabaseView();
                break;
            }
        }
    }

    private void HandleDatabaseEditorCancel()
    {
        switch (_dbView)
        {
            case DbView.EditorCreateFolder:
                _dbView = _dbEditorParentFolder == null ? DbView.FolderList : DbView.SubfolderList;
                _dbEditorParentFolder = null;
                break;
            case DbView.EditorCreateDoc:
                _dbView = _dbCurrentSubfolder != null ? DbView.DocumentList : DbView.SubfolderList;
                break;
            case DbView.EditorEditDoc:
                _dbView = DbView.DocumentViewer;
                break;
        }
        RefreshDatabaseView();
    }

    /// <summary>
    /// #Misfits Add - Wraps the body input's current selection with the supplied open/close
    /// BBCode tags. If nothing is selected, inserts the tag pair at the cursor and places
    /// the caret between them so the user can immediately type the wrapped text.
    /// </summary>
    private void WrapBBCode(string openTag, string closeTag)
    {
        // Only meaningful when the body input is visible (document edit/create).
        if (!DatabaseEditorBodyInput.Visible)
            return;

        var lower = DatabaseEditorBodyInput.SelectionLower.Index;
        var upper = DatabaseEditorBodyInput.SelectionUpper.Index;

        if (upper > lower)
        {
            // Wrap selected text. InsertAtCursor replaces the current selection.
            var full = Rope.Collapse(DatabaseEditorBodyInput.TextRope);
            var selected = full.Substring(lower, upper - lower);
            DatabaseEditorBodyInput.InsertAtCursor(openTag + selected + closeTag);
        }
        else
        {
            // No selection: insert tag pair and drop the caret between the two tags.
            DatabaseEditorBodyInput.InsertAtCursor(openTag + closeTag);
            var caret = lower + openTag.Length;
            DatabaseEditorBodyInput.CursorPosition = new TextEdit.CursorPos(caret, TextEdit.LineBreakBias.Top);
        }

        DatabaseEditorBodyInput.GrabKeyboardFocus();
    }

    /// <summary>
    /// #Misfits Add - Strips every [tag] / [/tag] pattern from the body input. Useful
    /// when an author wants to start fresh after over-formatting.
    /// </summary>
    private void StripBBCode()
    {
        if (!DatabaseEditorBodyInput.Visible)
            return;

        var full = Rope.Collapse(DatabaseEditorBodyInput.TextRope);
        // Remove anything that looks like a BBCode tag: `[...]` not containing `[` or newline.
        var cleaned = System.Text.RegularExpressions.Regex.Replace(full, @"\[/?[^\[\]\n]+\]", string.Empty);
        DatabaseEditorBodyInput.TextRope = new Rope.Leaf(cleaned);
    }

